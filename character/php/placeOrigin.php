<?php
/*Place of Origin*/




function placeOfOrigin ($val)
{
    if($val == '0')
    {
        return 'Lankhmar';
    }

    if($val == '1')
    {
        return 'The Land of the Eight Cities';
    }

    if($val == '2')
    {
        return 'The Cold Waste';
    }

    if($val == '3')
    {
        return 'The Eastern Lands';
    }

    if($val == '4')
    {
        return 'The Mingol Steppes';
    }


}


